void solve(){
    int n;cin>>n;
    vector<int>v(n);
    for(int i=0;i<n;i++){
        cin>>v[i];
    }
    sort(all(v));
    int fu=0;
    int z=0;
    for(int i=0;i<n-1;i++){
        if(v[i]+fu+z<0){
            fu+=v[i]+fu;
            z=0;
        }else{
            z=v[i]+fu+z;
        }
    }
    cout<<v[n-1]+fu+z<<endl;
}
