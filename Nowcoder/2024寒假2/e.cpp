void solve(){
    int n;cin>>n;
    vector<int>v(n);
    for(int i=0;i<n;i++){
        cin>>v[i];
    }
    int ans=0;
    for(int i=v.size()-1;i>=0;){
        int j=i-1;
        // cout<<i<<' '<<ans<<endl;
        while(j>=0&&v[j]==v[i]){
            j--;
        }
        if(j==-1)ans+=i+1;
        else ans++;
        i=j-1;
    }
    cout<<ans<<endl;
}
