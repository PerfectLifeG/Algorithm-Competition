int ksm(int a,int n){
    int r=1ll;
    while(n){
        if(n&1)r=r*a%mod;
        a=a*a%mod;
        n>>=1;
    }
    return r;
}
int inv(int a){
    return ksm(a,mod-2);
}
struct DSU {
    std::vector<int> f, siz;
    
    DSU() {}
    DSU(int n) {
        init(n);
    }
    
    void init(int n) {
        f.resize(n);
        std::iota(f.begin(), f.end(), 0);
        siz.assign(n, 1);
    }
    
    int find(int x) {
        while (x != f[x]) {
            x = f[x] = f[f[x]];
        }
        return x;
    }
    
    bool same(int x, int y) {
        return find(x) == find(y);
    }
    
    bool merge(int x, int y) {
        x = find(x);
        y = find(y);
        if (x == y) {
            return false;
        }
        siz[x] += siz[y];
        f[y] = x;
        return true;
    }
    
    int size(int x) {
        return siz[find(x)];
    }
};
void solve(){
    int n;cin>>n;
    DSU dsu(n+1);
    for(int i=0;i<n-1;i++){
        int a,b;cin>>a>>b;
        dsu.merge(a,b);
    }
    vector<int>v(n+1);
    for(int i=1;i<=n;i++){
        v[dsu.find(i)]++;
    }
    int ans=1;
    for(int i=0;i<=n;i++){
        ans=(ans*(v[i]+v[i]*v[i-1]%mod*inv(2)%mod+2)%mod)%mod;
    }
    cout<<ans<<endl;
}
