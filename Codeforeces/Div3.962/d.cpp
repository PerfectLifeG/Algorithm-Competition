void solve(){
    int n,x;cin>>n>>x;

    int ans=0;
    for(int a=1;a<=n;a++){
        for(int b=1;a*b<=n&&a+b<=x;b++){
            ans+=min(x-a-b,(n-a*b)/(a+b));
        }
    }
    cout<<ans<<endl;
}
