int ksm(int a,int n){
    int r=1ll;
    while(n){
        if(n&1)r=r*a%mod;
        a=a*a%mod;
        n>>=1;
    }
    return r;
} 
int inv(int a){
    return ksm(a,mod-2);
}
void solve(){
    int n;cin>>n;
    int p=(inv(n)+inv(n)*inv(n-1)%mod)%mod;p%=mod;
    int ans=((n-1)*(p+1)%mod+p+mod)%mod*inv(n*p%mod)%mod;ans%=mod;
    cout<<ans<<endl;
}
